#include <stdio.h>

int main() {
    // Initialise variables
    int a = 10;
    int b = 20;
    int c = 30;

    // Add a and b, store the result in d
    int d = a + b;
    printf("Value of d (a + b): %d\n", d);

    // Subtract c from d, store the result in e
    int e = d - c;
    printf("Value of e (d - c): %d\n", e);

    return 0;
}